@get('/api/users')
async def api_get_users():
    users = await User.findAll(orderBy='created_at desc')
    for u in users:
        u.passwd = '******'
    return dict(users = users)

__RE_EMAIL = re.compile(r'^[a-z0-9\.\-\_]+\@[a-z0-9\-\_]+(\.[a-z0-9\-\_]+){1,4}$')
__RE_SHA1 = re.compile(r'^[0-9a-f]{40}$')


@post('/api/users')
async def api_register_user(*, email, name, passwd):
    if not name or not name.strip():
        raise APIValueError('name')
    if not email or not __RE_EMAIL.match(email):
        raise APIValueError('email')
    if not passwd or not __RE_SHA1.match(passwd):
        raise APIValueError('passwd')
    users = await User.findAll('email = ?', [email])
    if len(users) > 0:
        raise APIValueError('register:failed', 'email', 'email is already in use.')
    uid = next_id()


